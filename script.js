    let scoreCreative = s.creative + s.active + (s.creative * 0.5);

    let winnerGroup = 'SCIENCE';
    let maxScore = scoreSci;
    if (scoreMathArt > maxScore) { maxScore = scoreMathArt; winnerGroup = 'MATH_ART'; }
