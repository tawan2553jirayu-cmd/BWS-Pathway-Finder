    if(checked.length < 40) return alert(`‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏ï‡∏≠‡∏ö‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö (‡πÄ‡∏´‡∏•‡∏∑‡∏≠ ${40 - checked.length} ‡∏Ç‡πâ‡∏≠)`);

